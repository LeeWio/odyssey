import fs from "node:fs/promises";
import path from "node:path";
import postcss from "postcss";

const THEMES = ["mouve", "brutalism", "glass"];
const ROOT_DIR = process.cwd();
const THEME_SOURCE_DIR = path.join(ROOT_DIR, "node_modules/@heroui-pro/react/dist/css/themes");
const OUTPUT_FILE = path.join(ROOT_DIR, "styles/themes/heroui-pro-themes.scoped.css");
const SCOPED_LAYER_NAMES = new Set(["base", "components"]);
const SKIPPED_LAYER_NAMES = new Set(["theme", "properties"]);
const SKIPPED_AT_RULE_NAMES = new Set(["keyframes", "-webkit-keyframes", "property"]);

function getThemeRootSelectorCandidates(theme) {
  return [
    `.${theme}-light`,
    `.${theme}-dark`,
    `html.${theme}-light`,
    `html.${theme}-dark`,
    `[data-theme=${theme}-light]`,
    `[data-theme=${theme}-dark]`,
    `[data-theme="${theme}-light"]`,
    `[data-theme="${theme}-dark"]`,
    `html[data-theme=${theme}-light]`,
    `html[data-theme=${theme}-dark]`,
    `html[data-theme="${theme}-light"]`,
    `html[data-theme="${theme}-dark"]`,
    `html[data-theme^="${theme}-"]`,
  ];
}

function getAncestorLayerName(rule) {
  let node = rule.parent;

  while (node) {
    if (node.type === "atrule" && node.name === "layer") {
      return node.params.trim().split(".")[0];
    }

    node = node.parent;
  }

  return null;
}

function hasAncestorAtRule(rule, names) {
  let node = rule.parent;

  while (node) {
    if (node.type === "atrule" && names.has(node.name)) {
      return true;
    }

    node = node.parent;
  }

  return false;
}

function isThemeRootSelector(selector, theme) {
  return selector
    .split(",")
    .map((item) => item.trim())
    .every((item) => {
      return getThemeRootSelectorCandidates(theme).includes(item);
    });
}

function isThemeScopedSelector(selector, theme) {
  const trimmed = selector.trim();

  return getThemeRootSelectorCandidates(theme).some((candidate) => {
    return (
      trimmed === candidate ||
      trimmed.startsWith(`${candidate} `) ||
      trimmed.startsWith(`${candidate}:`) ||
      trimmed.startsWith(`${candidate}.`) ||
      trimmed.startsWith(`${candidate}[`)
    );
  });
}

function shouldScopeRule(rule, theme) {
  if (hasAncestorAtRule(rule, SKIPPED_AT_RULE_NAMES)) return false;

  const layerName = getAncestorLayerName(rule);

  if (layerName && SKIPPED_LAYER_NAMES.has(layerName)) return false;
  if (layerName && !SCOPED_LAYER_NAMES.has(layerName)) return false;
  if (isThemeRootSelector(rule.selector, theme)) return false;

  return true;
}

function scopeSelector(selector, theme) {
  const prefix = `:where([data-theme^="${theme}-"])`;

  if (isThemeScopedSelector(selector, theme)) return selector;

  return selector.startsWith(prefix) ? selector : `${prefix} ${selector}`;
}

function scopeThemeCss(css, theme) {
  const root = postcss.parse(css, { from: `${theme}/index.css` });

  root.walkRules((rule) => {
    if (!shouldScopeRule(rule, theme)) return;

    rule.selectors = rule.selectors.map((selector) => scopeSelector(selector.trim(), theme));
  });

  return root.toString();
}

function assertNoLeakingSelectors(css) {
  const root = postcss.parse(css, { from: OUTPUT_FILE });
  const leaks = [];

  root.walkRules((rule) => {
    if (hasAncestorAtRule(rule, SKIPPED_AT_RULE_NAMES)) return;

    const layerName = getAncestorLayerName(rule);

    if (layerName && SKIPPED_LAYER_NAMES.has(layerName)) return;
    if (THEMES.some((theme) => isThemeRootSelector(rule.selector, theme))) return;

    const isScoped = rule.selectors.every(
      (selector) =>
        selector.trim().startsWith(":where([data-theme^=") ||
        THEMES.some((theme) => isThemeScopedSelector(selector, theme))
    );

    if (!isScoped) {
      leaks.push(rule.selector);
    }
  });

  if (leaks.length > 0) {
    throw new Error(
      `Generated HeroUI Pro theme CSS still has unscoped selectors:\n${leaks
        .slice(0, 20)
        .join("\n")}`
    );
  }
}

const sections = [];

for (const theme of THEMES) {
  const sourceFile = path.join(THEME_SOURCE_DIR, theme, "index.css");
  const source = await fs.readFile(sourceFile, "utf8");

  sections.push(`/* ${theme} */\n${scopeThemeCss(source, theme)}`);
}

const output = [
  "/* This file is generated by scripts/scope-heroui-pro-themes.mjs. */",
  "/* It scopes HeroUI Pro theme component overrides to their active data-theme. */",
  ...sections,
  [
    'html[data-theme^="brutalism-"] {',
    "  --brutalism-font-body: var(--font-brutalism-body);",
    "  --brutalism-font-display: var(--font-brutalism-display);",
    "  --font-sans: var(--font-brutalism-body);",
    "}",
  ].join("\n"),
].join("\n\n");

assertNoLeakingSelectors(output);

await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
await fs.writeFile(OUTPUT_FILE, `${output}\n`);

console.log(`Generated ${path.relative(ROOT_DIR, OUTPUT_FILE)}`);
